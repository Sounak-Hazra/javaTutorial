public class conditionaloperators {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;
        boolean c = a > b;
        System.out.println(c);


        c = a < b;
        System.out.println(c);


        c = a == b;
        System.out.println(c);


        c = a >= b;
        System.out.println(c);


        c = a <= b;
        System.out.println(c);


        c = a != b;
        System.out.println(c);

    }
}